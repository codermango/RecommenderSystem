package mark;

public class Genre {
	private String genreId;
	private String genreName;
	private int genreWeight;
	private boolean isOwned;
	/**
	 * @return the genreId
	 */
	public String getGenreId() {
		return genreId;
	}
	/**
	 * @param genreId the genreId to set
	 */
	public void setGenreId(String genreId) {
		this.genreId = genreId;
	}
	/**
	 * @return the genreName
	 */
	public String getGenreName() {
		return genreName;
	}
	/**
	 * @param genreName the genreName to set
	 */
	public void setGenreName(String genreName) {
		this.genreName = genreName;
	}
	/**
	 * @return the genreWeight
	 */
	public int getGenreWeight() {
		return genreWeight;
	}
	/**
	 * @param genreWeight the genreWeight to set
	 */
	public void setGenreWeight(int genreWeight) {
		this.genreWeight = genreWeight;
	}
	/**
	 * @return the isOwned
	 */
	public boolean isOwned() {
		return isOwned;
	}
	/**
	 * @param isOwned the isOwned to set
	 */
	public void setOwned(boolean isOwned) {
		this.isOwned = isOwned;
	}
	
	
}
